/**
 * Definition for singly-linked list.
 * public class ListNode {
 * int val;
 * ListNode next;
 * ListNode() {}
 * ListNode(int val) { this.val = val; }
 * ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution {
    public ListNode mergeTwoLists(ListNode l1, ListNode l2) {
        return method1(l1, l2);


//        return method2(l1, l2);
    }

    // é€’å½’æ–¹æ³•ï¼Œä»£ç æ˜“æ‡‚ã€‚æ—¶ç©ºåº¦ï¼šO(n), O(n)ï¼Œç©ºé—´éœ€æ±‚è¾ƒå¤§
    private ListNode method2(ListNode l1, ListNode l2) {
        if (l1 == null) {
            return l2;
        }
        if (l2 == null) {
            return l1;
        }

        if (l1.val < l2.val) {
            l1.next = mergeTwoLists(l1.next, l2);
            return l1;
        } else {
            l2.next = mergeTwoLists(l1, l2.next);
            return l2;
        }
    }

    // è‡ªå·±å®ç°çš„éé€’å½’æ–¹æ³•ã€‚ä»£ç ä¸å®¹æ˜“ç†è§£ï¼Œæ—¶ç©ºåº¦ï¼šO(n), O(1)
    private ListNode method1(ListNode l1, ListNode l2) {
        if (l1 == null) {
            return l2;
        }
        if (l2 == null) {
            return l1;
        }

        ListNode head = new ListNode();
        ListNode p = head;

        while (l1 != null && l2 != null) {
            if (l1.val > l2.val) {
                p.next = l2;
                l2 = l2.next;
            } else {
                p.next = l1;
                l1 = l1.next;
            }
            p = p.next;
        }

        p.next = l1 == null ? l2 : l1;
        return head.next;
    }
}
